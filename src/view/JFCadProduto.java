    private void jtfPesquisaProdutoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfPesquisaProdutoKeyReleased
    try {
            listaDeProdutos = DAO.pesquisar(jtfPesquisaProduto.getText());
            DefaultTableModel mp = (DefaultTableModel) jtProduto.getModel();
            mp.setNumRows(0);
        for (Produto u : listaDeProdutos) {
            mp.addRow(new String[]{"" + u.getIdProduto(), u.getNomeProduto(),"" + u.getCustoProduto(),"" +  u.getQtdProduto()}); //,u.getCustoProduto(), u.getQtdProduto()
        }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao pesquisar:" + e.getMessage());
        }
    }//GEN-LAST:event_jtfPesquisaProdutoKeyReleased

