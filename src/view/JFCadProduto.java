    private void jbExcluirProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirProdutoActionPerformed
       try {
            if (JOptionPane.showConfirmDialog(this, "Deseja realmente excluir o registro?") == JOptionPane.YES_OPTION) {
                if (DAO.excluir(Integer.parseInt(jlIdProduto.getText()))) {
                    JOptionPane.showMessageDialog(this, "Excluído.");
                    jbCancelarProdutoActionPerformed(evt);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao excluir: " + e.getMessage());
        }
    }//GEN-LAST:event_jbExcluirProdutoActionPerformed

    private void jbSalvarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarProdutoActionPerformed
        try {
            if (validarCampos()) {
                
                if (preencherObjeto()) {
                    
                    if (acao == 1) {
                        if (DAO.incluir(objProduto)) {
                            JOptionPane.showMessageDialog(this, "Salvo com sucesso!",
                                    "Meu sistema", JOptionPane.INFORMATION_MESSAGE);
                            //salvar no banco
                            jbCancelarProdutoActionPerformed(evt);
                        }
                    }
                    if (acao == 2) {
                        if (DAO.editar(objProduto)) {
                            JOptionPane.showMessageDialog(this, "Editado com sucesso!",
                                    "Meu sistema", JOptionPane.INFORMATION_MESSAGE);
                            //salvar no banco
                            jbCancelarProdutoActionPerformed(evt);
                        }
                    }
                }
            }
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, "Erro ao salvar:" + erro.getMessage(), "Sistema", JOptionPane.ERROR_MESSAGE);

        }
     
    }//GEN-LAST:event_jbSalvarProdutoActionPerformed

