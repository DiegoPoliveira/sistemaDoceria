        private boolean validarCampos() {
            String msg = new String();
            if (jtfNome.getText().trim().length() < 3) {
                msg = "\nVerifique o nome!";
            }
            if (jtfCusto.getText().trim().length() == 0) {
                msg = "\nVerifique o custo!";
            }
            if (jtfQuantidade.getText().trim().length() == 0) {
                msg = "\nVerifique a quantidade!";
            }

            if (msg.isEmpty()) {
                return true;
            } else {
                JOptionPane.showMessageDialog(this, "erros" + msg,"\nValidação", JOptionPane.ERROR_MESSAGE);
                return false;
            }

        }
    
        private boolean preencherObjeto() throws Exception { 
        objProduto.setIdProduto(Integer.parseInt(jlIdProduto.getText()));
        objProduto.setNomeProduto(jtfNome.getText());        
        objProduto.setCustoProduto(Double.parseDouble(jtfCusto.getText()));        
        objProduto.setQtdProduto(Integer.parseInt(jtfQuantidade.getText()));

        return true;
        }

